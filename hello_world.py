"""
เกี่ยวกับการจัดการ Whitespace ด้านขวาของสตริงใน Python:

- Whitespace พิเศษ (เช่น ช่องว่าง) อาจทำให้เกิดความสับสนในการเขียนโปรแกรม
- Python มองว่าสตริงที่มีและไม่มี Whitespace พิเศษเป็นคนละสตริงกัน
- ใช้เมธอด `rstrip()` เพื่อตัดช่องว่างด้านขวาของสตริงออก
- `rstrip()` จะคืนสตริงใหม่ที่ไม่มีช่องว่างทางขวา แต่ไม่แก้ไขสตริงต้นฉบับ

การใช้ `rstrip()` ช่วยให้จัดการกับ Whitespace ด้านขวาของสตริงได้ง่ายขึ้น ซึ่งมีประโยชน์ในการเปรียบเทียบหรือประมวลผลสตริง
"""

text = "Hello "
print(text.rstrip())  # Output: "Hello"
print(text)           # Output: "Hello "