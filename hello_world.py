"""
เกี่ยวกับประโยชน์ของเมธอด `lower()` ในการจัดเก็บข้อมูล String:

- เมธอด `lower()` ช่วยแปลง String ให้เป็นตัวพิมพ์เล็กทั้งหมด
- มักใช้ `lower()` กับข้อมูลที่รับมาจากผู้ใช้ก่อนนำไปจัดเก็บ เพื่อให้ไม่ต้องสนใจเรื่องตัวพิมพ์ใหญ่-เล็ก
- เมื่อต้องการแสดงผลข้อมูล สามารถแปลงเคสให้เหมาะสมได้ภายหลัง เช่น ใช้ `title()` เพื่อแปลงเป็นตัวพิมพ์ใหญ่ตัวแรกของแต่ละคำ

การใช้เมธอด `lower()` ช่วยให้การจัดเก็บและจัดการข้อมูล String เป็นไปอย่างสะดวกและยืดหยุ่นมากขึ้น
"""

name = input("กรุณาป้อนชื่อของคุณ: ")
stored_name = name.lower()
print("สวัสดี", stored_name.title())