"""
เพิ่มเติมเกี่ยวกับเมธอดสำหรับจัดการตัวพิมพ์ใหญ่-เล็กใน String 
- `upper()` : แปลง String ทั้งหมดเป็นตัวพิมพ์ใหญ่
- `lower()` : แปลง String ทั้งหมดเป็นตัวพิมพ์เล็ก
- เมธอดเหล่านี้มีประโยชน์ เช่น ทำให้โปรแกรมรู้จักคำที่เขียนต่างกรณีว่าเป็นคำเดียวกัน

เมธอดช่วยให้การทำงานกับ String ในเรื่องของตัวพิมพ์ใหญ่-เล็กเป็นไปได้ง่ายและสะดวกขึ้น
"""

name = "Ada Lovelace"
print(name.upper())  # ผลลัพธ์: "ADA LOVELACE"
print(name.lower())  # ผลลัพธ์: "ada lovelace"