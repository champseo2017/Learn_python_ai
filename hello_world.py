"""
เกี่ยวกับการเปลี่ยนตัวพิมพ์ใหญ่-เล็กใน String ด้วยเมธอดใน Python ดังนี้ครับ

- เมธอด (Method) คือ การกระทำที่ Python สามารถทำกับข้อมูลได้
- เมธอด `title()` ใช้สำหรับเปลี่ยนตัวอักษรตัวแรกของแต่ละคำใน String ให้เป็นตัวพิมพ์ใหญ่
- วิธีใช้งาน: เรียกใช้เมธอดต่อท้ายชื่อตัวแปร String คั่นด้วยจุด เช่น `name.title()`

เมธอดช่วยให้เราสามารถจัดการและเปลี่ยนแปลงข้อมูลใน String ได้อย่างสะดวก 
"""

name = "ada lovelace"
print(name.title())  # ผลลัพธ์: "Ada Lovelace"